import folium

def createMapaPlot(ubicacion, radius=50000):
    m = folium.Map(
        location=ubicacion, 
        zoom_start=4,  # Ajustar el nivel de zoom para enfocar la ubicación
        tiles='https://{s}.basemaps.cartocdn.com/dark_all/{z}/{x}/{y}{r}.png',
        attr='&copy; <a href="https://carto.com/attributions">CARTO</a>'
    )
    folium.Circle(
        location=ubicacion,
        radius=radius,  # Radio en metros
        color='red',   # Color del borde del círculo
        fill=True,      # Relleno del círculo
        fill_color='red',  # Color de relleno
        fill_opacity=0.5  # Opacidad del relleno
    ).add_to(m)
    
    folium.LayerControl().add_to(m)
    
    m.save('map.html')

